//--------------------------------------------------
// Description  : Set Tcon3 invert enable
// Input Value  : bEnable --> enable
// Output Value : None
//--------------------------------------------------
void ScalerTcon3InvEnable(bit bEnable)
{
    ScalerSetDataPortBit(P15_A3_TCON_ADDR, _P15_A4_PT_48_TC3_CTRL, ~(_BIT6), ((BYTE)bEnable << 6));
}



