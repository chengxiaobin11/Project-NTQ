//****************************************************************************
// FUNCTION DEFINITIONS
//****************************************************************************
//--------------------------------------------------
// Description  : Set Osd 2 font function
// Input Value  : bEnable -> _ENABLE or _DISABLE
// Output Value : None
//--------------------------------------------------
void ScalerOsd2FontFunction(bit bEnable)
{
    ScalerSetBit(P3A_18_FRAME_CTRL_18, ~_BIT3, ((BYTE)bEnable << 3));
}
